/**
 * Like Promise.all(items.map(fn)), but runs at most `limit` calls at once
 * instead of firing everything simultaneously. Used for per-stop weather
 * lookups so a large stop count doesn't burst dozens of parallel requests at
 * the weather API in one shot.
 */
export async function mapWithConcurrency<T, R>(
  items: T[],
  limit: number,
  fn: (item: T, index: number) => Promise<R>,
): Promise<R[]> {
  const results: R[] = new Array(items.length);
  let nextIndex = 0;

  async function worker() {
    while (nextIndex < items.length) {
      const current = nextIndex++;
      results[current] = await fn(items[current], current);
    }
  }

  const workers = Array.from({ length: Math.min(limit, items.length) }, worker);
  await Promise.all(workers);
  return results;
}
